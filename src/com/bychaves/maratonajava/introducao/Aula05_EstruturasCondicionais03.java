package com.bychaves.maratonajava.introducao;

public class Aula05_EstruturasCondicionais03 {
    public static void main(String[] args) {

        double salario = 6000;

        // Operador ternário: (condicao) ? verdadeiro : falso
        String resultado = salario > 5000 ? "Eu vou doar 500 pro DevDojo" : "Ainda nao tenho condições, mas vou ter!";

        System.out.println(resultado);
    }
}
